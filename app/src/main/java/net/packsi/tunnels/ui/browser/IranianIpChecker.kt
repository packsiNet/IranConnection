    // Synchronous — call only from a background thread (e.g. shouldInterceptRequest)
    fun isIranianHost(host: String): Boolean {
        cache[host]?.let { return it }
        val result = try {
            val addresses = InetAddress.getAllByName(host)
            addresses.any { addr ->
                val ip = addr.hostAddress ?: return@any false
                if (':' in ip) false else isIranianIp(ip) // skip IPv6
            }
        } catch (_: Exception) {
            false
        }
        if (cache.size > 500) cache.clear()
        cache[host] = result
        return result
    }

    fun isIranianIp(ip: String): Boolean {
        val ipLong = ipToLong(ip) ?: return false
        return CIDRS.any { (network, bits) ->
            val mask = if (bits == 0) 0L else (0xFFFFFFFFL shl (32 - bits)) and 0xFFFFFFFFL
            (ipLong and mask) == (network and mask)
        }
    }

    private fun ipToLong(ip: String): Long? = try {
        val p = ip.trim().split(".")
        if (p.size != 4) null
        else p.fold(0L) { acc, s -> (acc shl 8) or s.toLong() }
    } catch (_: Exception) { null }

    private fun parseCidr(cidr: String): Pair<Long, Int> {
        val i = cidr.indexOf('/')
        return (ipToLong(cidr.substring(0, i)) ?: 0L) to cidr.substring(i + 1).toInt()
    }
